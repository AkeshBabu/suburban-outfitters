        die("Connection failed: " . $conn->connect_error);
    }

    // Sanitize and validate the data
    $productName = sanitizeMySQL($conn, $_POST['productName']);
    $productPrice = sanitizeMySQL($conn, $_POST['productPrice']);
    $productCategory = sanitizeMySQL($conn, $_POST['productCategory']);
    $vendorId = sanitizeMySQL($conn, $_POST['vendorId']);

    // Prepare and bind
    $stmt = $conn->prepare("INSERT INTO products (product_name, price, category, vendor_id) VALUES (?, ?, ?, ?)");
    $stmt->bind_param("sdss", $productName, $productPrice, $productCategory, $vendorId);

    // Execute and check for success
    if ($stmt->execute()) {
        echo "<script>alert('New product added successfully!'); window.location.href='add-product.php';</script>";
    } else {
        echo "Error: " . $stmt->error;
    }

    // Close statement and connection
    $stmt->close();
    $conn->close();
}
